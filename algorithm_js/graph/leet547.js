class Graph {
  constructor(size) {
    this.root = new Array(size).fill(0).reduce((acc, _, idx) => {
      acc.push(idx);
      return acc;
    }, []);
    this.height = new Array(size).fill(1);
    this.count = size;
  }

  find(v) {
    if (this.root[v] === v) return v;
    this.root[v] = this.find(this.root[v]);
    return this.root[v];
  }

  union(x, y) {
    const rootX = this.find(x);
    const rootY = this.find(y);
    if (rootX === rootY) return;
    if (this.height[rootX] > this.height[rootY]) {
      this.root[rootY] = rootX;
    } else if (this.height[rootX] < this.height[rootY]) {
      this.root[rootX] = rootY;
    } else {
      this.root[rootY] = rootX;
      this.height[rootX] += 1;
    }
    this.count -= 1;
  }

  get provinceNumber() {
    return this.count;
  }
}

const findCircleNum = (isConnected) => {
  if (isConnected.length === 1) return 1;

  const graph = new Graph(isConnected.length);

  for (let i = 0; i < isConnected.length - 1; i++) {
    for (let j = i + 1; j < isConnected.length; j++) {
      if (!isConnected[i][j]) continue;
      graph.union(i, j);
    }
  }
  return graph.provinceNumber;
};

